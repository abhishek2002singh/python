#comparison operators compare two value and return boolean value (true or false)

# == - equal to
# != - not equal to 
# > - greater than
# < - less than
# >= - greater than or equal to
# <= - less than or equal to
print(10 == 5)  # equal to
print(10 != 5)  # not equal to  
print(10 > 5)  # greater than
print(10 < 5)  # less than
print(10 >= 5)  # greater than or equal to
print(10 <= 5)  # less than or equal to
