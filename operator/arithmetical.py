# operator -- operators is a symbol that performs specific task in operands and prodices some result

# arithmetic operators - arithmetic operators are used to perform mathematical operations like addition, subtraction, multiplication etc.

# + - addition
# - - subtraction
# * - multiplication
# / - division (its gave float value)
# // - floor division (its gave integer value)
# % - modulus (its gave remainder value)
# ** - exponentiation (its gave power value)


print(10 + 5)  # addition
print(10 - 5)  # subtraction
print(10 * 5)  # multiplication
print(10 / 5)  # division
print(10 // 3)  # floor division
print(10 % 3)  # modulus
print(2 ** 3)  # exponentiation
